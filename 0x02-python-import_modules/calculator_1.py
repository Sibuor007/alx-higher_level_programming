#!/usr/bin/python3
def add(a, b):
    """
    Args ( a: first integer b: second integer)
    return value. a + b
    """
    return (a + b)

def sub(a, b):
    """
    Args ( a: first integer b: second integer)
    return value. a - b
    """
    return (a - b)

def mul(a, b):
    """
    Args ( a: first integer b: second integer)
    return value. a * b
    """
    return (a * b)

def div(a, b):
    """
    Args ( a: first integer b: second integer)
    return value. a / b
    """
    return int(a / b)
